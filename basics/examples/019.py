# Operatory porównania

result = 10 == 8 # operator porównania, czy obie wartości są takie same
print(result)   # False

print( 10 == 10 )   # True
print( 10 != 8 )    # True, operator nierówności zwraca True jeśli oprandy nie
                    # są sobie równe

print( 4 > 1 )      # True, bo 4 jest większe od 1
print( 4 > 7 )      # False, bo 4 nie jest większe od 7
print( 3 < 5 )      # True, bo 3 jest mniejsze od 5

# Większe równe zwraca true, jeśli wartości jest większa lub równa
print( 10 >= 10 )   # True bo 10 większe lub równe 10
print( 11 >= 10 )   # True bo 11 większe lub równe 10

print( 5 <= 3 )     # mniejsze lub równe False
